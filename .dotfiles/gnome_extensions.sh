# Installing Gnome Shell Extensions
mkdir $HOME/Documents/github/scratch/extensions
##	Dash To Dock
wget "https://extensions.gnome.org/download-extension/dash-to-dock@micxgx.gmail.com.shell-extension.zip?version_tag=6553" 
mv "$HOME/Documents/github/scratch/extensions/dash-to-dock@micxgx.gmail.com.shell-extension.zip?version_tag=6553" \
 "$HOME/Documents/github/scratch/extensions/dash-to-dock@micxgx.gmail.com.shell-extension.zip?version_tag=6553.zip"
mkdir -p "$HOME/.local/share/gnome-shell/extensions/dash-to-dock@micxgx.gmail.com"
unzip -o "$HOME/Documents/github/scratch/extensions/dash-to-dock@micxgx.gmail.com" -d "$HOME/.local/share/gnome-shell/extensions/dash-to-dock@micxgx.gmail.com"

